def normalizar(series):
    """Normaliza a rango [0, 1]"""
    min_val = series.min()
    max_val = series.max()
    if max_val - min_val < 1e-9:
        return pd.Series(np.ones(len(series)), index=series.index)
    return (series - min_val) / (max_val - min_val)

