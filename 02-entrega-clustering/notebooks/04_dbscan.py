# Función de evaluación mejorada
def evaluar_dbscan_mejorado(X, eps_vals, ms_vals):
    """
    Evalúa combinaciones de DBSCAN con múltiples métricas
    Similar a find_optimal_parameters de HDBSCAN
    """
    resultados = []

    for eps in eps_vals:
        for ms in ms_vals:
            # Aplicar DBSCAN
            dbscan = DBSCAN(eps=eps, min_samples=ms)
            labels = dbscan.fit_predict(X)

            # Contar clusters y ruido
            n_clusters = len(set(labels)) - (1 if -1 in labels else 0)
            n_ruido = int(np.sum(labels == -1))
            pct_ruido = 100 * n_ruido / len(labels)

            # Filtros iniciales (similar a HDBSCAN)
            if n_clusters < 2 or n_clusters > 8:
                continue
            if pct_ruido > 50:  # Relajado: HDBSCAN usa 30%, pero damos más margen a DBSCAN
                continue

            # Validar que hay suficientes puntos no-ruido
            mask_no_ruido = labels != -1
            if np.sum(mask_no_ruido) <= n_clusters:
                continue

            # Equilibrio de clusters
            tamaños_clusters = []
            for cluster_id in set(labels):
                if cluster_id != -1:
                    tamaños_clusters.append(np.sum(labels == cluster_id))

            if tamaños_clusters:
                equilibrio = np.std(tamaños_clusters) / (np.mean(tamaños_clusters) + 1e-9)
                pct_mayor = 100 * max(tamaños_clusters) / np.sum(mask_no_ruido)

                # Rechazar si un cluster domina (igual que HDBSCAN)
                if pct_mayor > 80:
                    continue
            else:
                equilibrio = float('inf')
                pct_mayor = 100.0

            # Calcular métricas de calidad
            try:
                sil = silhouette_score(X[mask_no_ruido], labels[mask_no_ruido])
                db = davies_bouldin_score(X[mask_no_ruido], labels[mask_no_ruido])
                ch = calinski_harabasz_score(X[mask_no_ruido], labels[mask_no_ruido])
            except:
                continue

            resultados.append({
                'eps': round(eps, 4),
                'min_samples': ms,
                'n_clusters': n_clusters,
                'n_ruido': n_ruido,
                'pct_ruido': round(pct_ruido, 1),
                'pct_mayor': round(pct_mayor, 1),
                'equilibrio': round(equilibrio, 4),
                'silhouette': round(sil, 4),
                'davies_bouldin': round(db, 4),
                'calinski_harabasz': round(ch, 4)
            })

    return resultados

